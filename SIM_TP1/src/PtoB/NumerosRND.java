    private void btn_generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generarActionPerformed

        cantNumeros = Integer.parseInt(txt_cantValores.getText());
        GeneradorRND r= new GeneradorRND(cantNumeros);

        vec = new String[cantNumeros];
        vecGrafico= new double[cantNumeros];

        DecimalFormat truncado = new DecimalFormat("0.0000");

        datos = new DefaultTableModel(new Object[]{"numero", "Xi+1"}, 0);
        jTable2.setModel(datos);

        for (int i = 0; i < cantNumeros; i++) {

            double g =  r.getRandom();
            vecGrafico[i] = g;
            vec[i] = truncado.format(g) + "\n";

            // System.out.println("ver vec: "+vec[i]);
            //System.out.println("ver vecGrafico: "+ vecGrafico[i]);
            // Coinciden

        }

        if(Rbtn10.isSelected()==true){
            this.intervalo=10;
        }if (RBtn5.isSelected()==true){
            this.intervalo=5;
        }if (RBtn20.isSelected()==true){
            this.intervalo=20;
        }
        for (int i = 0; i < cantNumeros; i++) {
            Object[] fila = new Object[2];
            fila[0] = (i + 1);
            fila[1] = vec[i];

            datos.addRow(fila);
        }

    }//GEN-LAST:event_btn_generarActionPerformed

    private void btn_GraficoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GraficoActionPerformed

        try{

            Histograma histo = new Histograma("Histograma", vecGrafico, intervalo);
            histo.pack();
            RefineryUtilities.centerFrameOnScreen(histo);
            histo.setVisible(true);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "No se ingresaron los datos", "Error Histograma", WIDTH);
        }
        catch(ClassCastException e){
            JOptionPane.showMessageDialog(rootPane, "No se ingresaron los datos", "Error Histograma", WIDTH);
        }

    }//GEN-LAST:event_btn_GraficoActionPerformed

